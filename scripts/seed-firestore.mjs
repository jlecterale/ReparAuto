async function seedCollection(name, docs) {
  const snap = await db.collection(name).limit(1).get();
  if (!snap.empty) {
    console.log(`[seed] '${name}' already has data — skipping`);
    return;
  }
  if (DRY_RUN) {
    console.log(`[seed] would seed ${docs.length} docs into '${name}'`);
    return;
  }
  const batch = db.batch();
  for (const data of docs) {
    batch.set(db.collection(name).doc(), { ...data, dataCriacao: Timestamp.now() });
  }
  await batch.commit();
  console.log(`[seed] seeded ${docs.length} docs into '${name}'`);
}

